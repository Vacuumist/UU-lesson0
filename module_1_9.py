# Homework for Module 1, topic "Strings and String Indexing".
example = "This is an example string"
print(example[0])       # Outputs the first character
print(example[-1])      # Outputs the last character
print(example[12:])     # Outputs the 2nd half
print(example[::-1])    # Outputs the string in reverse order
print(example[1::2])    # Outputs every second character
